/**
 * Lightweight i18n — no dependencies, reactive via Svelte 5 runes.
 * `t(key)` reads `localeStore.current` during render, so components
 * re-render when the locale changes. Missing keys fall back to English,
 * then to the key itself (visible in dev, never a crash).
 */

export type Locale = 'en' | 'pt' | 'es' | 'zh' | 'pl'

export const LOCALES: { id: Locale; label: string }[] = [
  { id: 'en', label: 'English' },
  { id: 'pt', label: 'Português' },
  { id: 'es', label: 'Español' },
  { id: 'zh', label: '中文' },
  { id: 'pl', label: 'Polski' },
]

function detectLocale(): Locale {
  const saved = localStorage.getItem('sytra-locale') as Locale | null
  if (saved && LOCALES.some(l => l.id === saved)) return saved
  const nav = (navigator.language || 'en').toLowerCase()
  if (nav.startsWith('pt')) return 'pt'
  if (nav.startsWith('es')) return 'es'
  if (nav.startsWith('zh')) return 'zh'
  if (nav.startsWith('pl')) return 'pl'
  return 'en'
}

export const localeStore = $state<{ current: Locale }>({ current: detectLocale() })

export function setLocale(l: Locale) {
  localeStore.current = l
  localStorage.setItem('sytra-locale', l)
}

